// Activity 2: Assignment Operators

// Task 6: Use the += operator to add a number to a variable and log the result to the console.log
let number_1 = 10;
number_1 += 20;
console.log("By the use of (+=) operator the sum of number_1 is: ", number_1);

// Task 7: Use the -= operator to subtract a number from a variable and log the result to the console.log
let number_2 = 10;
number_2 -= 20;
console.log(
  "By the use of (-=) operator the subtraction of number_2 is: ",
  number_2
);
