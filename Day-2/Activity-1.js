// Task 1:  Write a program to add two numbers and log the result to the console.
let number_1 = 10;
let number_2 = 20;
let addNumbers = number_1 + number_2;
console.log(`The sum of ${number_1} and ${number_2} is: `, addNumbers);

// Task 2: Write a program to subtract two numbers and log the result to the console.log
let number_3 = 10;
let number_4 = 20;
let subtractNumbers = number_3 - number_4;
console.log(
  `The subtraction of ${number_3} and ${number_4} is: `,
  subtractNumbers
);

