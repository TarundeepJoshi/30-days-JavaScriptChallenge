// Activity 2: Array Methods (Basic)

// Task 3: Use the push method to add a new number to the end of the array and log the updated array.
const pushMethod = [1, 2, 4, 5, 6];
pushMethod.push(45);
console.log(pushMethod);

// Task 4: Use the pop method to remove the last element from the array and log the updated array.
const popMethod = [12, "for", " last"];
popMethod.pop();
console.log(popMethod);

// Task 5: Use the shift method to remove the first element from the array and log the updated array.
const shiftMethod = [1, 2, 3, 5, 6];
shiftMethod.shift();
console.log(shiftMethod);

// Task 6: Use the unshift method to add a new number to the beginning of the array and log the updated array.
const unshiftMethod = [1, 2, 3, 5, 6];
unshiftMethod.unshift(45);
console.log(unshiftMethod);
