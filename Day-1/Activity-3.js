// Activity 3: Data Types

// Task 4: Create variables of different data types (number, string, boolean, object, array) and log each variable's type using the typeof operator.

// Number
const age = 20;
console.log("type of age is", typeof age);

// String
const fullName = "Tarundeep Joshi";
console.log("type of fullName is", typeof fullName);

// Boolean
const isTrue = true;
console.log("type of isTrue is", typeof isTrue);

// Object
const object = {
  name: "Tarun",
  age: 20,
  class: "BCA",
};
console.log("type of object is", typeof obj);

// Array
const fruitsArray = ["Apple", "Pear", "Mango"];
console.log("type of fruitsArray is", typeof fruitsArray);
